
public class Java_05_if_else {

	public static void main(String[] args) {
		int n1 = 5;
		int n2 = 7;
		
		//if
		if(n1 < n2) {
			System.out.println("n1 > n2 is ture");
		}
		
		//if ~ else
		if(n1 == n2) {
			System.out.println("n1 == n2 is ture");
		}
		else {
			System.out.println("n1 == n2 is false");
		}

	}

}
